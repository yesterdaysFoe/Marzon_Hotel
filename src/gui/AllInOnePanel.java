    private void findMenus(int firstResult, String keyword) {
        List<ProductsAndServices> list = Initializer.getProductsAndServicesJpaController().findTransactionTypeWithKey(ProductEnum.RESTAURANT_MENU.getName(), keyword, firstResult, NO_ROWS);
        if (list.isEmpty()) {
            if (firstResult > 0) {
                menusFirstResult -= NO_ROWS;
            }
            if (firstResult < 0) {
                menusFirstResult += NO_ROWS;
            }
            Messages.showInfoMessage(this, "No More!");
        } else {
            DefaultTableModel dtm = (DefaultTableModel) jTable5.getModel();
            dtm.setRowCount(0);
            for (ProductsAndServices ps : list) {
                Object[] row = {ps.getName(), ps.getDetails(), ps.getPriceString()};
                dtm.addRow(row);
            }
        }
    }

    private void findServices(int firstResult, String keyword) {
        List<ProductsAndServices> list = Initializer.getProductsAndServicesJpaController().findTransactionTypeWithKey(ProductEnum.HOTEL_SERVICE.getName(), keyword, firstResult, NO_ROWS);
        if (list.isEmpty()) {
            if (firstResult > 0) {
                servicesFirstResult -= NO_ROWS;
            }
            if (firstResult < 0) {
                servicesFirstResult += NO_ROWS;
            }
            Messages.showInfoMessage(this, "No More!");
        } else {
            DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
            dtm.setRowCount(0);
            for (ProductsAndServices ps : list) {
                Object[] row = {ps.getName(), ps.getDetails(), ps.getPriceString()};
                dtm.addRow(row);
            }
        }
    }
