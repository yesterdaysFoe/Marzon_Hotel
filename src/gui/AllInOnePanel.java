    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        try {
            clearFields();
            trasaction = Initializer.getTransactionsJpaController().findTransactionsEager(Integer.parseInt(jTextField10.getText()));
            getTransactionPayments().addAll(trasaction.getTransactionPaymentsCollection());
            getTransactionRooms().addAll(trasaction.getTransactionRoomsCollection());
            getTransactionProducts().addAll(trasaction.getTransactionProductsCollection());
            jTextField5.setText(trasaction.getCustomerFirstName() + trasaction.getCustomerLastName());
            jTextField6.setText(trasaction.getAddress());
            jTextField7.setText(trasaction.getCustomerTelNo());
            jTextField8.setText(trasaction.getCustomerEmail());

            double rooms = 0;
            for (TransactionRooms tr : getTransactionRooms()) {
                rooms += tr.getTotalPrice();
            }

            double products = 0;
            for (TransactionProducts tp : getTransactionProducts()) {
                products += tp.getTotalPrice();
            }

            double payments = 0;
            for (TransactionPayments tp : getTransactionPayments()) {
                payments += tp.getAmount();
            }
            final double total = rooms + products;
            final double vat = total * 0.12;
            final double balance = total - payments;

            jTextField1.setText(Initializer.df.format(vat));
            jTextField2.setText(Initializer.df.format(total - vat));
            jTextField3.setText(Initializer.df.format(payments));
            jTextField4.setText(total - payments > 0 ? Initializer.df.format(balance) : Initializer.df.format(0));
            jTextField9.setText(Initializer.df.format(total));

            showPanel(jPanel2, "accountCard");
            jTextField10.setText(null);
        } catch (NumberFormatException e) {
            Messages.showErrorMessage(this, "Invalid Transaction Code");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

