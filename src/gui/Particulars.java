package gui;

public class Particulars {

    private String name;
    private String amount;
    private String roomNo;
    private String cusName;
    private String dateStayed;
    private String resBill;
    private String hotelBill;
    private String totalBill;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getRoomNo() {
        return roomNo;
    }

    public void setRoomNo(String roomNo) {
        this.roomNo = roomNo;
    }

    public String getCusName() {
        return cusName;
    }

    public void setCusName(String cusName) {
        this.cusName = cusName;
    }

    public String getDateStayed() {
        return dateStayed;
    }

    public void setDateStayed(String dateStayed) {
        this.dateStayed = dateStayed;
    }

    public String getResBill() {
        return resBill;
    }

    public void setResBill(String resBill) {
        this.resBill = resBill;
    }

    public String getHotelBill() {
        return hotelBill;
    }

    public void setHotelBill(String hotelBill) {
        this.hotelBill = hotelBill;
    }

    public String getTotalBill() {
        return totalBill;
    }

    public void setTotalBill(String totalBill) {
        this.totalBill = totalBill;
    }
    
    

}
